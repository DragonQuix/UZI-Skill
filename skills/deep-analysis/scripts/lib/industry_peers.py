def resolve_industry_name(industry: str, aggregate_siblings: bool = False) -> str | list[str] | None:
    """将任意来源的行业名解析为 INDUSTRY_PEERS 的有效 key(s)。

    v3.10 · 集中解析：替代旧版分散在 fetch_similar_stocks / fetch_industry /
    fetch_peers 三处的别名表。

    匹配优先级（三级级联）：
      1. 精确命中 → 直接返回 key
      2. 别名映射 → _INDUSTRY_ALIASES 查表
      3. 包含匹配 → key in industry 或 industry in key（反向子串）

    Args:
        industry: 任意来源的行业名（如东财 "白酒" / 雪球 "饮料" / 申万 "白酒"）
        aggregate_siblings: True → 返回所有包含 industry 子串的 key 列表
                           （用于聚合细粒度子分类，如 "银行" → ["股份制银行","城商行",...]）

    Returns:
        单个 key 字符串（aggregate_siblings=False），或 key 列表（True），或 None
    """
    if not industry or not isinstance(industry, str) or len(industry.strip()) < 2:
        return None

    industry = industry.strip()

    # 1. 精确命中
    if industry in INDUSTRY_PEERS:
        return [industry] if aggregate_siblings else industry

    # 2. 别名映射
    alias = _INDUSTRY_ALIASES.get(industry)
    if alias and alias in INDUSTRY_PEERS:
        return [alias] if aggregate_siblings else alias

    # 3. 包含匹配（industry 作为子串出现在 key 中，或 key 作为子串出现在 industry 中）
    candidates = []
    for key in INDUSTRY_PEERS:
        if len(industry) >= 2 and len(key) >= 2:
            if industry in key or key in industry:
                candidates.append(key)

    if not candidates:
        # 最后兜底：前 2-3 字匹配
        prefix = industry[:3] if len(industry) >= 3 else industry[:2]
        for key in INDUSTRY_PEERS:
            if key.startswith(prefix) or prefix in key:
                candidates.append(key)
                if len(candidates) >= 5:
                    break

    if aggregate_siblings:
        return candidates if candidates else None
    return candidates[0] if candidates else None


def get_peer_codes(industry: str) -> list[str]:
    """为指定行业返回纯股票代码列表。

    v3.10 · 通过 resolve_industry_name 做三级匹配（精确→别名→包含），
    替代旧版的裸 dict.get()，消除 '白酒' vs '饮料' 等分类体系不匹配问题。
    """
    resolved = resolve_industry_name(industry)
    if not resolved:
        return []
    if isinstance(resolved, list):
        # 聚合多个子分类
        codes: list[str] = []
        seen: set[str] = set()
        for key in resolved:
            for code, _ in INDUSTRY_PEERS.get(key, []):
                if code not in seen:
                    codes.append(code)
                    seen.add(code)
        return codes
    return [code for code, _ in INDUSTRY_PEERS.get(resolved, [])]


